package model.yxt;

public class StudyPlanPhaseKnowModel {

    private String name;

    private Integer phaseStudyHours;

    private Integer phaseOrderIndex;

    private String mastertitle;

    private Integer studyHours;

    private Integer orderIndex;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getPhaseStudyHours() {
        return phaseStudyHours;
    }

    public void setPhaseStudyHours(Integer phaseStudyHours) {
        this.phaseStudyHours = phaseStudyHours;
    }

    public Integer getPhaseOrderIndex() {
        return phaseOrderIndex;
    }

    public void setPhaseOrderIndex(Integer phaseOrderIndex) {
        this.phaseOrderIndex = phaseOrderIndex;
    }

    public String getMastertitle() {
        return mastertitle;
    }

    public void setMastertitle(String mastertitle) {
        this.mastertitle = mastertitle;
    }

    public Integer getStudyHours() {
        return studyHours;
    }

    public void setStudyHours(Integer studyHours) {
        this.studyHours = studyHours;
    }

    public Integer getOrderIndex() {
        return orderIndex;
    }

    public void setOrderIndex(Integer orderIndex) {
        this.orderIndex = orderIndex;
    }
}
